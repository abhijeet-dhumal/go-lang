package main

import "fmt"

func main() {
	fmt.Println("Abhijeet")
	fmt.Println("Length of text 'Abhijeet':", len("Abhijeet"))
	fmt.Println("Ascii value of letter 'A' on 0th index in word 'Abhijeet':", "Abhijeet"[0])
	fmt.Println("Ascii value of letter 'b' on 1st index in word 'Abhijeet':", "Abhijeet"[1])
}

/* >>
Abhijeet
Length of text 'Abhijeet': 8
Ascii value of letter 'A' on 0th index in word 'Abhijeet': 65
Ascii value of letter 'b' on 1st index in word 'Abhijeet': 98
*/
